public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");

        double myDouble = 5/2;
        System.out.println(myDouble); // output 2.0
        int b = (int) 2.9 + (int) + 1.8;
        System.out.println(b); // output 3

        System.out.println((double) 10/4);
        System.out.println(10/4.0); // output 2.5
        System.out.println(10%4); // output 2

        int x =1;
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);

        int y = 10;
        y += 3; // y = y+3
        System.out.println(y);
    }
}
